export function chain(
  a: unknown,
  ab?: Fn,
  bc?: Fn,
  cd?: Fn,
  de?: Fn,
  ef?: Fn,
  fg?: Fn,
  gh?: Fn,
  hi?: Fn,
): unknown {
  switch (arguments.length) {
    case 1:
      return a

    default: {
      let ret = arguments[0]
      for (let i = 1; i < arguments.length; i++) {
        ret = arguments[i](ret)
      }

      return Array.from(ret)
    }
  }
}


